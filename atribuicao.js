const a = 7
let b = 3
b += a // b = b + a
console.log(b)

b -= 4 // b = b -4 (o novo b, já que let é uma variável)
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b %= 2 // b = b % 2 - se o resultado for par, o valor será 0, se for impar, será 1
console.log(b)

